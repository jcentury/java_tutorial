package numberstring;

public class CharString {

    public static void main(String[]args){
        //문자 ''작은따옴표 사용
        System.out.println('생');
        //문자열 "" 큰따옴표 사용
        System.out.println("생활코딩");
        //오류
        // System.out.println('생활코딩');
        //문자도 문자열에 포함
        System.out.println("생");
        //숫자도 문자열로 표현 가능 값 = 11
        System.out.println("1"+"1");
        //문자를 더할 때 + 사용
        System.out.println("생"+"활");
        //""큰따옴표 표시를 위해서는 \ 사용
        System.out.println("I said \"Welcome to java\"");
        //줄바꿈을 위해서는 \n 사용4
        System.out.println("HTML\nCSS\nJAVA");
    }
    
}
